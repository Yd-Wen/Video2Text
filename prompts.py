VOCAB_INJECTION_TEMPLATE = """
## 词汇表纠错参考

在理解以下文本时，请注意这些可能的语音识别错误：

{vocab_list}

请根据上下文自动纠正常见的语音识别错误，使笔记内容更准确。
"""


# =============================================================================
# PromptBuilder 类
# =============================================================================

@dataclass
class PromptConfig:
    """提示词配置"""
    format_type: str  # note, weekly, diary
    model: str  # deepseek, openai, qwen
    temperature: Optional[float] = None
    max_length: Optional[int] = None
    language: str = "zh"


class PromptBuilder:
    """
    提示词构建器

    根据笔记格式类型组装完整的提示词，包含：
    - 系统角色设定
    - 输出格式规范
    - Few-shot示例
    - 词汇表注入（可选）
    - 用户输入文本
    """

    def __init__(self, config: PromptConfig):
        self.config = config
        self._validate_format()

    def _validate_format(self) -> None:
        """验证格式类型是否有效"""
        valid_formats = ["note", "weekly", "diary"]
        if self.config.format_type not in valid_formats:
            raise ValueError(f"不支持的格式类型: {self.config.format_type}. "
                           f"支持的类型: {', '.join(valid_formats)}")

    def build_system_prompt(self) -> str:
        """构建系统提示词（角色设定 + 输出规范）"""
        format_type = self.config.format_type

        parts = [
            SYSTEM_ROLES[format_type],
            OUTPUT_FORMATS[format_type],
            FEW_SHOT_EXAMPLES[format_type]
        ]

        return "\n\n---\n\n".join(parts)

    def build_user_prompt(self, text: str, vocab: Optional[Dict[str, str]] = None) -> str:
        """
        构建用户提示词

        【参数】
            text: 预处理后的文本内容
            vocab: 词汇表（可选），用于语音识别纠错

        【返回】
            str: 完整的用户提示词
        """
        parts = []

        # 添加词汇表注入（如果有）
        if vocab:
            vocab_list = "\n".join([f"- \"{k}\" → \"{v}\"" for k, v in vocab.items()])
            parts.append(VOCAB_INJECTION_TEMPLATE.format(vocab_list=vocab_list))

        # 添加任务指令
        parts.append(f"""
## 任务

请将以下文本转换为**{self._get_format_name()}**格式的结构化笔记。

### 原始文本

{text}

### 要求

1. 仔细理解文本内容，提取关键信息
2. 按照上述输出格式规范组织内容
3. 保持语言简洁、结构清晰
4. 使用Markdown格式输出
5. {'保留原文语言' if self.config.language == 'zh' else '使用英文输出'}

请直接输出Markdown格式的笔记内容，不要添加额外说明。
""")

        return "\n\n".join(parts)

    def _get_format_name(self) -> str:
        """获取格式的中文名称"""
        names = {
            "note": "技术笔记",
            "weekly": "周报",
            "diary": "日记"
        }
        return names.get(self.config.format_type, "笔记")

    def build_messages(self, text: str, vocab: Optional[Dict[str, str]] = None) -> List[Dict[str, str]]:
        """
        构建完整的对话消息列表（用于OpenAI兼容API）

        【参数】
            text: 预处理后的文本内容
            vocab: 词汇表（可选）

        【返回】
            List[Dict[str, str]]: 消息列表，包含system和user消息
        """
        return [
            {"role": "system", "content": self.build_system_prompt()},
            {"role": "user", "content": self.build_user_prompt(text, vocab)}
        ]


# =============================================================================
# 便捷函数
# =============================================================================

def get_prompt_builder(format_type: str = "note",
                       model: str = "deepseek",
                       **kwargs) -> PromptBuilder:
    """
    便捷函数：创建PromptBuilder实例

    【参数】
        format_type: 笔记格式 (note/weekly/diary)
        model: 模型选择 (deepseek/openai/qwen)
        **kwargs: 其他配置参数

    【返回】
        PromptBuilder: 提示词构建器实例

    【示例】
        >>> builder = get_prompt_builder(format_type="weekly", model="deepseek")
        >>> messages = builder.build_messages(text="今天的会议内容...")
    """
    config = PromptConfig(
        format_type=format_type,
        model=model,
        **kwargs
    )
    return PromptBuilder(config)


